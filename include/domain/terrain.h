#pragma once

#include "include/domain/terrain_kind.h"

#include <stdint.h>

/*
 * Pure, deterministic, endless terrain generator (no furi, no I/O).
 *
 * Terrain is an infinite (x >= 0) sequence of contiguous ZONES. Each zone has a kind
 * (TerrainKind) and a length in x units; both are derived purely from `seed` and the zone's
 * index, with no cross-call mutable state. `terrain_at` walks zones from x = 0 until it finds
 * the one containing `x` — O(zones up to x), which is cheap for the distances an endless run
 * covers on-device.
 *
 * Difficulty ramps with x: the opening zones are always flat, and as x grows the odds of
 * drawing an uphill or obstacle zone increase (see the ramp constants in terrain.c).
 *
 * HEIGHT is a small "game unit" surface offset, bounded to [TERRAIN_HEIGHT_MIN,
 * TERRAIN_HEIGHT_MAX] — an integer band the renderer maps onto the 128x64 display's playable
 * strip. Everything here is integer arithmetic, kept fixed-point friendly for the Task 4 sim.
 */

enum {
    TERRAIN_HEIGHT_MIN = 0,
    TERRAIN_HEIGHT_MAX = 40,
};

typedef struct {
    TerrainKind kind;
    int16_t height;
} TerrainSample;

/* xorshift32: pure PRNG, no libc rand, no globals. `*state` must be non-zero (see
 * gurpil_rng_seed) — xorshift is stuck at zero forever otherwise. Advances `*state` in place
 * and returns the new value. */
uint32_t gurpil_rng_next(uint32_t *state);

/* Maps an arbitrary caller seed (including 0) to a valid, non-zero xorshift32 state. */
uint32_t gurpil_rng_seed(uint32_t seed);

/* Returns the terrain kind + surface height at position `x` (negative x is clamped to 0), for
 * the endless course generated by `seed`. Deterministic: a given (seed, x) always returns the
 * same sample, regardless of call order or how many other positions were queried before it. */
TerrainSample terrain_at(uint32_t seed, int32_t x);
